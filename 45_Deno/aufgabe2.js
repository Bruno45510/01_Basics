let input = prompt("Gib mir eine Zahl")

let input1 = parseInt(input)

if (input1 >= 0) {
    console.log("The number is positive");
    
}else console.log("The number is not positive");

if (input1 <= 0) {
    console.log("The number is negative");
    
}else console.log("The number is positive or 0");

if (input1 == 0) {
    console.log("The number is zero");
    
}else console.log("the number is not zero");
